/// Get all county names as a sorted list
List<String> getAllCounties() {
  final counties = kenyaLocations.keys.toList();
  counties.sort();
  return counties;
}

/// Get constituencies for a specific county
List<String>? getConstituencies(String county) {
  return kenyaLocations[county];
}

/// Check if a county exists
bool countyExists(String county) {
  return kenyaLocations.containsKey(county);
}

/// Get total number of counties
int getTotalCounties() {
  return kenyaLocations.length;
}

/// Search counties by name (case-insensitive)
List<String> searchCounties(String query) {
  if (query.isEmpty) return getAllCounties();
  
  final lowerQuery = query.toLowerCase();
  return kenyaLocations.keys
      .where((county) => county.toLowerCase().contains(lowerQuery))
      .toList()
    ..sort();
}